/**
 * Class that defines variables with default values.
 *
 * @abstract
 */
export abstract class Messages {

    public static readonly VALIDATION: any = {
        FIELD_NOT_PROVIDED: 'Este campo é requerido'
    }

    public static readonly INVITES: any = {
        INVITE_MSG: 'O usuário {0} convidou você para entrar no grupo: {1}.'
    }
}